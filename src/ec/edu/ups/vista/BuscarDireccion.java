    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Direccion d = cd.BuscarCodigo(Integer.parseInt(txtCodigo.getText()));
        if(d.getCallepreincipal() != null){
            txtCalleP.setText(d.getCallepreincipal());
            txtCalleS.setText(d.getCalleSecundaria());
            txtNumero.setText(String.valueOf(d.getNumero()));
            txtCedula.setText(d.getPersonaCedula());
        }else{
            JOptionPane.showMessageDialog(this, "No existe la direccion", "Buscar direccion", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


